#!/usr/bin/env python3
#The last printed expression is "_" in interactive mode.
print(2+2)
print(50-5*6)
print((50-5*6)/4)
print(8/5)        # division always returns a floating point number
print(17/3)       # classic division returns a float
print(17//3)      # floor division discards the fractional part
print(17%3)       # mod operator returns the remainder of division
print(5*3+2)      # result*divisor+remainder
print(5+3*2)      # process operators privilege automatically
print(5**2)       # 5 to power of 2
print(2**7)       # 2 to power of 7
print(4*3.75-1)   # convert integer operand to floating point
